    <h1 id="soaequi">STATE-OF-THE-ART EQUIPMENT</h1>

    <div id="soaleft">
    <h5>Concrete Batching Plant</h5>
    <p>It is accurate, big and most efficient industrial
    concrete production process.The equipment takes in
    coarse / the aggregate, Portland cement, admixture,
    water at predetermined quantity to give accurate
    mixture with good concrete qualities. Our plant
    can produce 30 cubic meter per hour.</p>

    <h5>Concrete placement pump</h5>
    <p>The equipment pumps concrete vertically (along a
    high rise building) and horizontally. Our concrete
    pump can place 80 cubic meter of concrete per hour,
    vertically (up to 20th floor) and horizontally
    (up to 1km stretch). The machine encourages mass
    concrete casting, in-site casting with difficult
    terrain (e.g basement, construction, inaccessible
    site etc).</p>

    <h5>Poker Vibrator</h5>
    <p>Poker vibrators are applied on concrete to
    agitate concrete for proper setting and good
    outlooks</p>
      </div>

  <div id="soaright">
    <h5>Concrete Mixers</h5>
    <p>It produces concrete of different strength
      (depending on the mix ratio, admixture etc) from
      fine / coarse aggregate, cement and water.
      We can process 50 cubic meter of  concrete
      in one hour.</p>

    <h5>Concrete Screeding Machine</h5>
    <p>Surface finish machine. The concrete surface is
    tamper with surface screed for even and good finish.</p>

    <h5>Concrete Cutter</h5>
    <p>Concrete / Bitumen Concrete Machine.
    It is used to cut (or trim) concrete or bitumen
    in desirable and undesirable circumstances.</p>

    <h5>Electric Vibrators.</h5>

    <h5>Power Trowel</h5>
    <p>Power Trowel is used to smoothing the
    “forming or cutting” concrete. When applied,
    it gives fair face to concrete.</p>
